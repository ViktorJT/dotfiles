#!/bin/bash

# Fancy logging functions for consistent output formatting
# Can be sourced by any script

log_header() {
  echo ""
  echo "┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┓"
  echo "┃                                                                              ┃"
  printf "┃ %-76s ┃\n" "🚀 $1"
  echo "┃                                                                              ┃"
  echo "┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛"
  echo ""
}

log_section() {
  echo ""
  echo "┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┓"
  printf "┃ %-76s ┃\n" "📌 $1"
  echo "┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛"
  echo ""
}

log_step() {
  printf "   %-74s \n" "🔹 $1"
}

log_success() {
  printf "   %-74s \n" "✅ $1"
}

log_warning() {
  printf "   %-74s \n" "⚠️  $1"
}

log_error() {
  printf "   %-74s \n" "❌ $1"
}

log_info() {
  printf "   %-74s \n" "ℹ️  $1"
}

log_command_output() {
  echo "   ┌────────────────────────────────────────────────────────────────────────────┐"
  while IFS= read -r line; do
    printf "   │ %-74s │\n" "$line"
  done
  echo "   └────────────────────────────────────────────────────────────────────────────┘"
}

log_code_block() {
  echo "   ┌────────────────────────────────────────────────────────────────────────────┐"
  echo "$1" | while IFS= read -r line; do
    printf "   │ %-74s │\n" "$line"
  done
  echo "   └────────────────────────────────────────────────────────────────────────────┘"
}

log_success_block() {
  echo ""
  echo "   ┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┓"
  echo "   ┃                                                                           ┃"
  echo "   ┃  🎉 $1                                                              ┃"
  echo "   ┃                                                                           ┃"
  echo "   ┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛"
  echo ""
}